#!/bin/bash,
# Azure AD App Registration Setup Commands,
,
# 1. Create App Registration,
az ad app create --display-name 'Meeting Minutes Bot' --sign-in-audience AzureADMyOrg,
,
# 2. Add Microsoft Graph API Permissions,
az ad app permission add --id YOUR_APP_ID --api 00000003-0000-0000-c000-000000000000 --api-permissions:,
# - OnlineMeetings.Read.All (df021288-bdef-4463-88db-98f22de89214),
# - OnlineMeetingTranscript.Read.All (30b87d18-ebb1-45db-97f8-82ccb1f0190c),
# - Chat.ReadWrite (9ff7295e-131b-4d94-90e1-69fde507ac11),
# - Files.ReadWrite.All (75359482-378d-4052-8f01-80520e7db3cd),
,
# 3. Grant Admin Consent,
az ad app permission admin-consent --id YOUR_APP_ID,
,
# 4. Create Client Secret,
az ad app credential reset --id YOUR_APP_ID --append